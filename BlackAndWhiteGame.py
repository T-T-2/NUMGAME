# TV프로그램 '더 지니어스'에 등장한 게임 '흑과 백'을 코드로 구현한다.
# 버전 0 : 실행 전 메뉴 구현
while True:
    # 사용자에게 메뉴를 안내하고 입력받는다.
    N = input("1: 숫자 카드 게임 실행 2: 게임 기록 보기 3: 종료\n")
    
    # 사용자 입력이 '1'인 경우 게임을 실행한다. (구현 예정)
    if N == '1':
        # 게임 로직은 이곳에 추가될 예정이다.
        print("게임 실행 기능은 이후 버전에서 추가됩니다.")
    
    # 사용자 입력이 '2'인 경우 게임 기록을 보여준다. (기록 보기 예정)
    elif N == '2':
        # 기록 보기 로직은 이곳에 추가될 예정이다.
        print("기록 보기 기능은 이후 버전에서 추가됩니다.")
    
    # 사용자 입력이 '3'인 경우 종료한다.
    elif N == '3':
        # break 문을 통해 루프를 종료하여 프로그램을 끝낸다.
        print("프로그램을 종료합니다.")
        break
    
    # 틀린 입력을 처리한다.
    else:
        print("잘못된 값입니다. 1, 2, 3 중에 입력하세요.")

